"""
YouTube thumbnail generator — bold typographic style.

Layout (1280×720):
  Left  ~45% (576px): black background + large bold hook text + red accent word
  Right ~55% (704px): AI-generated scene image (DALL-E 3) with fade edge

The hook text (2 lines white + 1 accent word in red) is generated by Claude Haiku
based on the video title and description.
The background image is generated by DALL-E 3 (falls back to AMV frame extraction).

Usage:
    python scripts/generate_thumbnail.py \
        --script-path output/run-001/script/script.json \
        --output-dir output/run-001
"""
import argparse
import json
import subprocess
import sys
import urllib.request
from pathlib import Path

import anthropic

sys.path.insert(0, str(Path(__file__).parent.parent))
from scripts.utils.config import load_config, get_ffmpeg_path
from scripts.utils.file_helpers import ensure_dir
from scripts.utils.state_manager import StateManager

FFMPEG = get_ffmpeg_path()


def parse_args():
    parser = argparse.ArgumentParser(description="Generate YouTube thumbnail")
    parser.add_argument("--script-path", required=True, help="Path to script.json")
    parser.add_argument("--output-dir", required=True, help="Output directory for this run")
    parser.add_argument("--highlight-frame", default=None,
                        help="Path to a specific frame image/video to use as the thumbnail background")
    return parser.parse_args()


# ---------------------------------------------------------------------------
# Frame extraction
# ---------------------------------------------------------------------------

def extract_highlight_frame(frames_dir: Path, thumbnail_dir: Path) -> Path | None:
    """Extract a still frame from the most dramatic scene in frames/."""
    video_exts = {".mp4", ".webm", ".mov"}
    image_exts = {".jpg", ".jpeg", ".png"}

    if not frames_dir.exists():
        return None

    all_files = sorted(frames_dir.iterdir())
    video_files = [f for f in all_files if f.suffix.lower() in video_exts]
    image_files = [f for f in all_files if f.suffix.lower() in image_exts]

    out = thumbnail_dir / "highlight_frame.jpg"

    if video_files:
        # Pick second scene if available — often more dramatic than the intro
        pick = video_files[1] if len(video_files) > 1 else video_files[0]
        try:
            subprocess.run(
                [FFMPEG, "-y", "-i", str(pick), "-ss", "00:00:01",
                 "-vframes", "1", "-q:v", "2", str(out)],
                check=True, capture_output=True,
            )
            return out
        except Exception as e:
            print(f"  ffmpeg frame extract failed: {e}")

    if image_files:
        pick = image_files[1] if len(image_files) > 1 else image_files[0]
        try:
            from PIL import Image
            Image.open(pick).convert("RGB").save(str(out), "JPEG")
            return out
        except Exception as e:
            print(f"  Image copy failed: {e}")

    return None


# ---------------------------------------------------------------------------
# DALL-E 3 image generation
# ---------------------------------------------------------------------------

def generate_dalle_frame(script: dict, out_path: Path, config: dict) -> Path | None:
    """Generate thumbnail background image using DALL-E 3."""
    api_key = config.get("openai_api_key")
    if not api_key:
        print("  No OPENAI_API_KEY — skipping DALL-E generation")
        return None

    try:
        from openai import OpenAI

        content_type = script.get("content_type", "anime")
        scenes = script.get("scenes", [])

        # Pick a visually interesting scene (skip rank_transition and intro)
        candidates = [
            s["visual_prompt"]
            for s in scenes
            if s.get("scene_type") not in ("rank_transition", "intro")
            and s.get("visual_prompt")
        ]
        visual = candidates[len(candidates) // 2] if candidates else script.get("title", "")

        if content_type == "bedtime-story":
            style = (
                "children's storybook illustration, soft watercolor style, "
                "warm golden lighting, dreamy cozy atmosphere, gentle pastel colors, "
                "cute characters, magical mood"
            )
        else:
            style = (
                "anime cinematic art, dramatic studio lighting, ultra-detailed, "
                "vibrant saturated colors, MAPPA-style quality, epic atmosphere, "
                "dynamic heroic composition, character prominently visible"
            )

        prompt = (
            f"{visual}. {style}. "
            "Right-panel composition with strong visual focus. "
            "No text, no watermarks, no logos, no subtitles."
        )

        client = OpenAI(api_key=api_key)
        response = client.images.generate(
            model="dall-e-3",
            prompt=prompt[:4000],
            size="1792x1024",
            quality="standard",
            n=1,
        )

        url = response.data[0].url
        urllib.request.urlretrieve(url, str(out_path))
        return out_path

    except Exception as e:
        print(f"  DALL-E generation failed: {e}")
        return None


# ---------------------------------------------------------------------------
# Hook text generation
# ---------------------------------------------------------------------------

def generate_hook_text(title: str, description: str) -> tuple[list[str], str]:
    """
    Use Claude Haiku to produce 3 elements for the thumbnail:
      [line1, line2, accent_word]
    line1 + line2 are white; accent_word is red.
    Falls back to a generic hook if the API call fails.
    """
    try:
        client = anthropic.Anthropic()
        prompt = (
            "You are creating text for a YouTube anime video thumbnail in a bold typographic style.\n\n"
            f"Video title: {title}\n"
            f"Description: {description[:300]}\n\n"
            "Generate thumbnail hook text with exactly 3 elements:\n"
            "- Element 1: 1-2 words, ALL CAPS (hook setup, white text)\n"
            "- Element 2: 1-2 words, ALL CAPS (hook payoff, white text)\n"
            "- Element 3: 1 word, ALL CAPS (category/emotion, red accent)\n\n"
            "Rules:\n"
            "- Be punchy and click-worthy\n"
            "- Max 12 characters per element\n"
            "- Examples: [\"MUST\", \"WATCH\", \"ANIME\"] or [\"PURE\", \"POWER\", \"RANKING\"] "
            "or [\"DARK\", \"TRUTH\", \"REVEALED\"]\n\n"
            "Respond with ONLY a JSON array of exactly 3 strings. Nothing else."
        )
        msg = client.messages.create(
            model="claude-haiku-4-5-20251001",
            max_tokens=60,
            messages=[{"role": "user", "content": prompt}],
        )
        raw = msg.content[0].text.strip()
        # Strip markdown code fences if present
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]
        parts = json.loads(raw.strip())
        if isinstance(parts, list) and len(parts) == 3:
            return [str(p).upper() for p in parts[:2]], str(parts[2]).upper()
    except Exception as e:
        print(f"  Hook generation failed: {e} — using fallback")

    return ["MUST", "WATCH"], "ANIME"


